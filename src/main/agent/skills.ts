// 注入到 system，让模型按用户当前场景【自动匹配】对应能力（依赖 gpt-4o 的 function-calling/指令遵循，
// 无需用户显式指定、也无需独立的意图分类步骤）。新增能力＝在此加一条 + 必要时配套工具/知识常量。

