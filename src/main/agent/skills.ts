/** 组装成注入 system 的「可用能力」段落 */
export function skillsSystemBlock(): string {
  const lines = SKILLS.map((s) => `· 【${s.name}】当${s.when}\n  做法：${s.guide}`)
  return (
    `【可用能力 · 按场景自动启用】\n你除了做各类单据/表格、格式转换、抽表拆表、套模板、PDF 处理外，还具备下列能力。` +
    `请根据用户当前的需求自行判断该用哪一种，无需用户指定能力名称：\n${lines.join('\n')}`
  )
}
