// 墨童「能力(skill)」注册表。
// 工具(tools.ts)是确定性的本地执行；skill 是"何时该用什么能力 + 质量规范"的知识层，
// 注入到 system，让模型按用户当前场景【自动匹配】对应能力（依赖 gpt-4o 的 function-calling/指令遵循，
// 无需用户显式指定、也无需独立的意图分类步骤）。新增能力＝在此加一条 + 必要时配套工具/知识常量。

export interface Skill {
  id: string
  name: string
  /** 触发场景：用户说什么 / 给什么文件时启用这条能力 */
  when: string
  /** 启用后的做法、可用工具与质量规范 */
  guide: string
}

