export const CONTRACT_CHECKLIST = `【合同审查要点 · 逐条核对】

一、主体与基础
- 买卖双方名称/地址/联系方式是否完整、与营业执照/抬头一致；签约主体是否有权签署。
- 合同号、签订日期、签订地点是否齐全。

二、标的与品质/数量
- 货物名称、规格型号、数量、单位是否明确，与报价/订单一致。
- 品质标准（国标/行标/样品/图纸）与检验方法、检验机构、检验时间地点。
- 数量是否允许溢短装（如 ±5%）及计价方式。

三、价格与贸易术语（外贸重点）
- 单价、总价、币种是否明确；大小写是否一致。
- 贸易术语（Incoterms 2020：EXW/FOB/CIF/CIP/DAP 等）是否写明版本与指定地点；据此明确风险转移点、运费/保险/清关由谁承担。
- 是否含税、税率、税费由谁承担。

四、付款
- 付款方式（电汇 T/T、信用证 L/C、托收 D/P D/A、预付/尾款比例）、时间节点、账户信息。
- L/C：开证时间、有效期、交单期、是否可撤销、单证要求是否过苛（软条款风险）。
- 定金/预付款比例、退款条件；逾期付款的利息/违约责任。

五、交货与运输
- 交货时间、地点、分批交货是否允许；运输方式、装运港/目的港。
- 包装方式、唛头要求；木质包装是否需熏蒸/IPPC 标识。
- 迟延交货的宽限期与违约金计算。

六、单证（外贸）
- 所需单据清单：发票、装箱单、提单/运单、产地证(CO/FORM E 等)、报关单、保险单、质检证书。
- 单证提供时间与责任方。

七、验收、索赔与质保
- 验收标准与期限；不符约定时的处理（退换、补发、折价）。
- 索赔期限、索赔依据、赔偿上限。
- 质保期、质保范围与售后责任。

八、违约与责任
- 违约金/赔偿金标准是否对等、是否过高或过低；责任上限。
- 解除合同的条件与后果。

九、不可抗力
- 定义、通知义务与时限、证明文件、后果（顺延/解除）。

十、争议解决与法律适用
- 争议解决方式（协商/仲裁/诉讼）；仲裁机构（如 CIETAC）、地点、语言；或法院管辖地。
- 适用法律（中国法/CISG 公约等）是否明确。

十一、其他
- 知识产权归属与侵权责任；保密条款。
- 出口管制/制裁合规（最终用户与用途）；
- 合同份数、语言（中英文不一致时以何为准）、生效条件、附件效力、变更与转让限制。

十二、按合同类型额外关注（依实际合同类型挑选）
- 采购/供货合同：到货验收标准与期限、质保期与三包、付款与开票、延迟交货违约金、备品备件与售后。
- 服务/承揽合同：服务范围与交付标准(SOW)、里程碑与验收、成果知识产权归属、保密、分包/转包限制。
- 租赁合同：租期与续租、租金及递增、押金与退还条件、维修与费用承担、提前解约与违约、转租限制。
- 劳动/劳务合同：试用期与工资、工时与加班、社保公积金、竞业限制及补偿、解除条件与经济补偿、保密。
- 保密协议(NDA)：保密信息范围与例外、保密期限、使用目的限制、返还/销毁义务、违约责任与救济、单向/双向。

审查输出要求：逐条指出①缺失项 ②风险点（注明高/中/低）③具体修改建议；最后给一句总体结论。不要编造合同中没有的内容。`
